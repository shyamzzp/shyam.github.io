export function SuffleData(data: any[]) {
    let i = data.length - 1;
    for (; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        const temp = data[i];
        data[i] = data[j];
        data[j] = temp;
    }
    return data;
}

export function GetRandomColor() {
    const colors = [
        "rgb(255 45 85)",
        "rgb(45 181 93)",
        "rgb(255 184 0)",
    ];
    const randomIndex = Math.floor(Math.random() * colors.length);
    return colors[randomIndex];
}